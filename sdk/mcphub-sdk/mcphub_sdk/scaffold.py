# ---------------------------------------------------------------------------
# Public API (importable + testable)
# ---------------------------------------------------------------------------


def scaffold(
    name: str,
    *,
    out_dir: str | Path = ".",
    service_name: str | None = None,
    port: int = 8000,
) -> Path:
    """Generate a new SDK-based MCP server directory.

    Parameters
    ----------
    name:
        Server slug used for directory name, container name, service name, and
        the FastMCP server name (e.g. ``"my-service"`` → dir ``my-service/``,
        server name ``"my-service-mcp"``).
    out_dir:
        Parent directory to create ``<name>/`` inside.  Defaults to ``"."``.
    service_name:
        Optional human-readable service label used in comments.  Defaults to
        the uppercased, hyphen-stripped form of ``name`` (e.g.
        ``"my-service"`` → ``"MY_SERVICE"``).
    port:
        Host port to bind in the compose snippet (``127.0.0.1:<port>:8000``).
        Defaults to ``8000``.  The TODO comment reminds the operator to pick a
        free port.

    Returns
    -------
    Path
        Absolute path to the newly created ``<name>/`` directory.
    """
    svc = service_name or name.upper().replace("-", "_")
    out_path = Path(out_dir).resolve() / name
    out_path.mkdir(parents=True, exist_ok=False)

    files: dict[str, str] = {
        "server.py": _SERVER_PY.format(name=name, service_name_upper=svc),
        "Dockerfile": _DOCKERFILE,
        "requirements.txt": _REQUIREMENTS_TXT,
        "compose-snippet.yaml": _COMPOSE_SNIPPET.format(
            name=name,
            port=port,
            healthcheck_test=_HEALTHCHECK_TEST,
        ),
    }

    for filename, content in files.items():
        (out_path / filename).write_text(content, encoding="utf-8")

    print(
        _NEXT_STEPS.format(
            count=len(files),
            out_path=out_path,
            name=name,
            service_name_upper=svc,
        )
    )

    return out_path


# ---------------------------------------------------------------------------
# CLI entry point  (python -m mcphub_sdk.scaffold)
# ---------------------------------------------------------------------------


def main(argv: list[str] | None = None) -> None:
    parser = argparse.ArgumentParser(
        prog="python -m mcphub_sdk.scaffold",
        description=(
            "Scaffold a new SDK-based MCP server.\n\n"
            "Creates ./<name>/ with server.py, Dockerfile, requirements.txt, "
            "and a compose-snippet.yaml to paste into podman-compose.lab.yml."
        ),
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument("name", help="Server slug (e.g. 'my-service')")
    parser.add_argument(
        "--service-name",
        dest="service_name",
        default=None,
        help="Human-readable service label used in comments (default: NAME uppercased).",
    )
    parser.add_argument(
        "--port",
        type=int,
        default=8000,
        help="Host port to use in the compose snippet (default: 8000).",
    )
    parser.add_argument(
        "--out-dir",
        dest="out_dir",
        default=".",
        help="Parent directory for the new server dir (default: current directory).",
    )
    args = parser.parse_args(argv)

    try:
        scaffold(
            args.name,
            out_dir=args.out_dir,
            service_name=args.service_name,
            port=args.port,
        )
    except FileExistsError:
        print(
            f"error: directory '{args.name}' already exists in '{args.out_dir}'. "
            "Remove it or choose a different name.",
            file=sys.stderr,
        )
        sys.exit(1)


if __name__ == "__main__":
    main()
