"""
mcphub_sdk.scaffold — Project scaffolder for new SDK-based MCP servers.

Usage
-----
    python -m mcphub_sdk.scaffold <name> [--service-name NAME] [--port PORT]

Creates ./<name>/ containing:
  server.py            — Runnable SDK stub (identity from identity(), NOT tool params)
  Dockerfile           — FROM mcphub-sdk:base, COPY server.py
  requirements.txt     — Extra deps only (base image already has mcp/uvicorn/httpx/mcphub_sdk)
  compose-snippet.yaml — Paste-ready service block for podman-compose.lab.yml

Security note (H3 / FO-1)
--------------------------
The generated server.py NEVER shows user_sub / caller / principal as tool parameters.
Identity is ONLY resolved via identity() which reads the proxy-injected X-User-Sub header.
A tool parameter for identity is forgeable — the SDK + proxy are the enforcement point.
"""
